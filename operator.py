# arithmetic operator
a = 10
b = 7
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a**b)

# relational operator
print(a < b)
print(a == b)
print(a != b)


# assignment operator
a += b
print(a)

# logical operator
# Not
rain = False
print(not (rain))

print(not (5 > 3))

# and
print((5 > 3) and (5 > 1))
print((5 > 2) or (6 > 8))
